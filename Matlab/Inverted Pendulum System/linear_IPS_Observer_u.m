dq_fun = @difeq_IPS_Observer;
tspan = [0:0.01:5];
z0 = [1;0;pi/3;0;1;0;pi/3;0];
m = 0.1;
M = 2;
l = 0.5;
g = 9.8;
n = 6;
P_Con = [-3+1i,-3-1i,-2+3j,-2-3j];
P_Ob = n * P_Con;
A = [0,1,0,0;
    0,0,-m*g/M,0;
    0,0,0,1;
    0,0,(m+M)*g/(M*l),0];
B = [0;1/M;0;-1/(M*l)];
C = [1,0,0,0;0,0,1,0];
K = place(A,B,P_Con);
L = place(A',C',P_Ob)';
A_bar = [A-B*K,B*K;
         zeros(size(A)),A-L*C];
[t,z] = ode45(dq_fun,tspan,z0,[],A_bar);
z_hat = z(:,1:4);
u = -K * z_hat';
plot(t,u,'linewidth',2);
xlabel('Time');
ylabel('Force');
title('Input Force of Linearized Inverted Pendulum System with Observer(6)');
grid on;
grid minor;