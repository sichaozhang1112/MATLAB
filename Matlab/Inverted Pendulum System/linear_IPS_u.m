dq_fun = @difeq_IPS;
tspan = [0:0.01:5];
y0 = [1;0;pi/3;0];
m = 0.1;
M = 2;
l = 0.5;
g = 9.8;
P = [-3+1i,-3-1i,-2+3j,-2-3j];
A = [0,1,0,0;
    0,0,-m*g/M,0;
    0,0,0,1;
    0,0,(m+M)*g/(M*l),0];
B = [0;1/M;0;-1/(M*l)];
K = place(A,B,P);
[t,y] = ode45(dq_fun,tspan,y0,[],K,A,B);
u = -K * y';
plot(t,u,'linewidth',2);
xlabel('Time');
ylabel('Force');
title('Input Force of Linearized Inverted Pendulum System with Full State Feedback Control');
grid on;
grid minor;
